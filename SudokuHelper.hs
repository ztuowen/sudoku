module SudokuHelper where

prettyPrint l a [] = ""
prettyPrint l a (x:xs)
    | a == l = "\n"++(show x)++" " ++ (prettyPrint l 1 xs)
    | otherwise = (show x)++" "++(prettyPrint l (a+1) xs)

